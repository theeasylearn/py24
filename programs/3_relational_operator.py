#relational operators
num1 = int(input("Enter num 1")) # 10
num2 = int(input("Enter num 2")) # 20

result = num1 == num2  # 10 == 20
print(f"{result} = {num1} == {num2}")

result = num1 != num2  # 10 != 20
print(f"{result} = {num1} != {num2}")

result = num1 < num2  # 10 != 20
print(f"{result} = {num1} < {num2}")

result = num1 > num2  # 10 != 20
print(f"{result} = {num1} > {num2}")

result = num1 <= num2  # 10 != 20
print(f"{result} = {num1} <= {num2}")

result = num1 >= num2  # 10 != 20
print(f"{result} = {num1} >= {num2}")

